# 자료형 - Dictionary 형 (Javascript의 dictionary형과 동일)

a_dict = {}
a_dict = {'name':'bob','age':21}
a_dict['height'] = 178

# a_dict의 값은? {'name':'bob','age':21, 'height':178}
print("a_dict:", a_dict)
# a_dict['name']의 값은? 'bob'
print("a_dict['name']:", a_dict['name'])
# a_dict['age']의 값은? 21
print("a_dict['age']:", a_dict['age'])
# a_dict['height']의 값은? 178
print("a_dict['height']:", a_dict['height'])