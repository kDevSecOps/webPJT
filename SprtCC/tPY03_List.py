# 자료형 - 리스트 형 (Javascript의 배열형과 동일)

a_list = []
a_list.append(1)     # 리스트에 값을 넣는다
a_list.append([2,3]) # 리스트에 [2,3]이라는 리스트를 다시 넣는다

# a_list의 값은? [1,[2,3]]
print("a_list:", a_list)
# a_list[0]의 값은? 1
print("a_list[0]:", a_list[0])
# a_list[1]의 값은? [2,3]
print("a_list[1]:", a_list[1])
# a_list[1][0]의 값은? 2
print("a_list[1][0]:", a_list[1][0])